// Get a random question based on tone and type
function getRandomQuestion(tone, type) {
    const questions = questionDatabase[tone]?.[type] || [];
    if (questions.length === 0) return null;

    const randomIndex = Math.floor(Math.random() * questions.length);
    return questions[randomIndex];
}

// Get a random question that hasn't been used yet
function getUniqueRandomQuestion(tone, type, usedQuestions = []) {
    const questions = questionDatabase[tone]?.[type] || [];
    const availableQuestions = questions.filter(q => !usedQuestions.includes(q));

    if (availableQuestions.length === 0) {
        // If all questions have been used, reset and start over
        return getRandomQuestion(tone, type);
    }

    const randomIndex = Math.floor(Math.random() * availableQuestions.length);
    return availableQuestions[randomIndex];
}

